/* ---- VM execution engine ---- */
typedef struct {
    uint8_t  stack[64];
    int      sp;
    const uint8_t *input;
    int      input_len;
} VM;

static void   vm_push(VM *v, uint8_t x) { v->stack[v->sp++] = x; }
static uint8_t vm_pop(VM *v)            { return v->stack[--v->sp]; }

static int vm_exec(VM *v, const uint8_t *code, int len)
{
    int ip = 0;
    while (ip < len) {
        uint8_t op = code[ip++];
        switch (op) {
        case OP_PUSH: vm_push(v, code[ip++]);            break;
        case OP_LOAD: vm_push(v, v->input[code[ip++]]); break;
        case OP_XOR:  { uint8_t b=vm_pop(v), a=vm_pop(v); vm_push(v, a^b);          break; }
        case OP_ADD:  { uint8_t b=vm_pop(v), a=vm_pop(v); vm_push(v, a+b);          break; }
        case OP_SUB:  { uint8_t b=vm_pop(v), a=vm_pop(v); vm_push(v, a-b);          break; }
        case OP_EQ:   { uint8_t b=vm_pop(v), a=vm_pop(v); vm_push(v, a==b ? 1 : 0); break; }
        case OP_AND:  { uint8_t b=vm_pop(v), a=vm_pop(v); vm_push(v, a&b);          break; }
        case OP_HALT: return (int)vm_pop(v);
        default:      return 0;
        }
    }
    return 0;
}

/* ---- Decode and print an obfuscated string ---- */
static void print_xor(const uint8_t *s, int n, uint8_t k)
{
    for (int i = 0; i < n; i++)
        putchar(s[i] ^ k);
}

int main(void)
{
    /* Anti-debug: ptrace(PTRACE_TRACEME) fails when a tracer is attached.
     * We don't branch immediately — the debugger result is used quietly
     * later to corrupt the decryption key. */
    long dbg = ptrace(PTRACE_TRACEME, 0, 0, 0);

    /* Print prompt (XOR-obfuscated) */
    print_xor(s_prompt, (int)sizeof(s_prompt), STR_MASK);
    fflush(stdout);

    /* Read input */
    char inp[64];
    memset(inp, 0, sizeof(inp));
    if (!fgets(inp, (int)sizeof(inp), stdin))
        return 1;
    int n = (int)strlen(inp);
    if (n > 0 && inp[n-1] == '\n') inp[--n] = 0;

    /* Decode the bytecode decryption key (stored XOR'd with KEY_MASK) */
    uint8_t key[16];
    for (int i = 0; i < 16; i++)
        key[i] = key_enc[i] ^ KEY_MASK;

    /* If debugger is attached, silently corrupt the key so VM produces
     * garbage — the binary appears to work normally but always says denied */
    if (dbg == -1) {
        for (int i = 0; i < 16; i++)
            key[i] ^= 0xFF;
    }

    /* Decrypt the bytecode */
    uint8_t bc[BC_LEN];
    for (int i = 0; i < BC_LEN; i++)
        bc[i] = bc_enc[i] ^ key[i % 16];

    /* Length check before running the VM */
    if (n != EXPECTED_LEN) {
        print_xor(s_fail, (int)sizeof(s_fail), STR_MASK);
        putchar('\n');
        return 1;
    }

    /* Run the VM */
    VM vm;
    memset(&vm, 0, sizeof(vm));
    vm.input     = (const uint8_t *)inp;
    vm.input_len = n;

    int ok = vm_exec(&vm, bc, BC_LEN);

    if (ok) {
        print_xor(s_ok,   (int)sizeof(s_ok),   STR_MASK);
    } else {
        print_xor(s_fail, (int)sizeof(s_fail),  STR_MASK);
    }
    putchar('\n');

    return ok ? 0 : 1;
}
