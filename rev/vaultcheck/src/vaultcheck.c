/*
 * vaultcheck — hard REV challenge
 *
 * A stack-based bytecode VM validates the password.
 * The bytecode is XOR-encrypted in the binary.
 * A ptrace anti-debug silently corrupts the decryption key
 * when a debugger is attached.
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdint.h>
#include <sys/ptrace.h>

/* ---- VM opcodes ---- */
#define OP_PUSH  0x01   /* PUSH  <byte>      push immediate */
#define OP_LOAD  0x02   /* LOAD  <idx>       push input[idx] */
#define OP_XOR   0x03   /* XOR               pop b,a; push a^b */
#define OP_ADD   0x04   /* ADD               pop b,a; push (a+b)&0xff */
#define OP_SUB   0x09   /* SUB               pop b,a; push (a-b)&0xff */
#define OP_EQ    0x05   /* EQ                pop b,a; push (a==b) */
#define OP_AND   0x06   /* AND               pop b,a; push a&b */
#define OP_HALT  0x08   /* HALT              return top-of-stack */

/* ---- Generated constants (do not edit by hand) ---- */
